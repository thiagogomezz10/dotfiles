-- Use the system clipboard
vim.opt.clipboard = "unnamedplus"
-- Enable line numbers
vim.opt.number = true
-- Enable relative line numbers
vim.opt.relativenumber = true
vim.opt.expandtab = true      -- Use spaces instead of tabs
vim.opt.tabstop = 4           -- A tab character is displayed as 4 spaces
vim.opt.shiftwidth = 4        -- Indent/outdent by 4 spaces
vim.opt.softtabstop = 4       -- Pressing Tab inserts/removes 4 spaces
vim.opt.smartindent = true    -- Optional: smart auto-indenting
